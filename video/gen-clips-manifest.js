// Turn public/clips/meta.json (written by record-v04.js) + the optional
// maintainer drop-in captures into src/clips-manifest.ts, the single timing
// source the v0.4 compositions read. Run before every render:
//
//   node gen-clips-manifest.js && npx remotion render src/index.ts PromoV04 ...
//
// Drop-in slots (real X screen recordings the maintainer captures later):
//   public/clips/bot-x-capture.mp4     the bot beat (tag → like → reply)
//   public/clips/x-export-capture.mp4  the archive-export beat (feature video)
// When a file is present it is probed with ffprobe and used verbatim by the
// comp; when absent the comp falls back to its animated mockup (bot beat) or
// skips the beat (export intro).

const { execFileSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const CLIPS_DIR = path.join(__dirname, "public", "clips");
const META_PATH = path.join(CLIPS_DIR, "meta.json");
const OUT_PATH = path.join(__dirname, "src", "clips-manifest.ts");

function probe(file) {
  try {
    const out = execFileSync(
      "ffprobe",
      [
        "-v", "error",
        "-select_streams", "v:0",
        "-show_entries", "stream=avg_frame_rate,duration,width,height",
        "-of", "json",
        file,
      ],
      { encoding: "utf8" }
    );
    const s = JSON.parse(out).streams?.[0];
    if (!s) return null;
    const [num, den] = String(s.avg_frame_rate || "30/1").split("/").map(Number);
    return {
      fps: den ? num / den : 30,
      durationSec: Number(s.duration) || 0,
      width: s.width,
      height: s.height,
    };
  } catch {
    return null;
  }
}

const meta = fs.existsSync(META_PATH)
  ? JSON.parse(fs.readFileSync(META_PATH, "utf8"))
  : {};

const recorded = {};
for (const name of ["demo", "bot-embed"]) {
  const file = path.join(CLIPS_DIR, `${name}.mp4`);
  if (!fs.existsSync(file)) {
    console.warn(`! missing recording public/clips/${name}.mp4`);
    continue;
  }
  const p = probe(file) || {};
  const m = meta[name] || {};
  recorded[name] = {
    src: `clips/${name}.mp4`,
    fps: m.fps ?? p.fps ?? 30,
    durationSec: m.durationSec ?? p.durationSec ?? 0,
    marks: m.marks ?? {},
  };
}

const dropIns = {};
for (const [key, file] of [
  ["botCapture", "bot-x-capture.mp4"],
  ["exportCapture", "x-export-capture.mp4"],
]) {
  const full = path.join(CLIPS_DIR, file);
  if (fs.existsSync(full)) {
    const p = probe(full);
    dropIns[key] = p
      ? { src: `clips/${file}`, fps: p.fps, durationSec: p.durationSec, width: p.width, height: p.height }
      : null;
    console.log(`✓ drop-in present: ${file}`);
  } else {
    dropIns[key] = null;
  }
}

const banner =
  "// GENERATED by gen-clips-manifest.js from public/clips/. Do not edit.\n" +
  "// Re-run `node gen-clips-manifest.js` after re-recording or after adding\n" +
  "// a drop-in capture (bot-x-capture.mp4 / x-export-capture.mp4).\n";

const body = `${banner}
export type RecordedClip = {
  src: string;
  fps: number;
  durationSec: number;
  marks: Record<string, number>;
};

export type DropInClip = {
  src: string;
  fps: number;
  durationSec: number;
  width: number;
  height: number;
};

export const RECORDED: Record<string, RecordedClip> = ${JSON.stringify(recorded, null, 2)};

export const DROP_INS: { botCapture: DropInClip | null; exportCapture: DropInClip | null } = ${JSON.stringify(dropIns, null, 2)};
`;

fs.writeFileSync(OUT_PATH, body);
console.log(`✓ ${OUT_PATH}`);
